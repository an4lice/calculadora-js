
var num1 = 10
var num2 = 5

function calcSomar(num1, num2){
    var resSoma = num1 + num2
    return resSoma
}

calcSomar(10, 5)

function calcSub(num1, num2){
    var resSub = num1 - num2
    return resSub
}

calcSub (10, 5)

function calcDiv(num1, num2){
    var resDiv = num1 / num2
    return resDiv
}

calcDiv(10, 5)

function calcMult(num1, num2){
    var resMult = num1 * num2
    return resMult
}

calcMult(10, 5)