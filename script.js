
var num1 = 10
var num2 = 5

function calcSomar(){
    var soma1 = num1
    var soma2 = num2
    var resSoma = soma1 + soma2
    return resSoma
}

function calcSub(){
    var sub1 = num1
    var sub2 = num2
    var resSub = sub1 - sub2
    return resSub
}

function calcDiv(){
    var div1 = num1
    var div2 = num2
    var resDiv = div1 / div2
    return resDiv
}

function calcMult(){
    var mult1 = num1
    var mult2 = num2
    var resMult = mult1 * mult2
    return resMult
}